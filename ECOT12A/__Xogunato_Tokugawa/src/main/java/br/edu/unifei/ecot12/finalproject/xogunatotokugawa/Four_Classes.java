package br.edu.unifei.ecot12.finalproject.xogunatotokugawa;

public abstract class Four_Classes extends Edo_Society{
	
	private String name;
	private String reputation;
	private String localReputation;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getReputation() {
		return reputation;
	}
	public void setReputation(String reputation) {
		this.reputation = reputation;
	}
	public String getLocalReputation() {
		return localReputation;
	}
	public void setLocalReputation(String localReputation) {
		this.localReputation = localReputation;
	}
	
	

}
